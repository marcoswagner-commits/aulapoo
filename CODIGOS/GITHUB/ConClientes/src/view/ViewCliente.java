    private void preencheDados()
    {
        int linhaS = tableClientes.getSelectedRow();
       
        if (linhaS < 0) {JOptionPane.showMessageDialog(this, "Selecione uma linha na tabela!");}
        else
        {
            int codigo = Integer.parseInt(tableClientes.getValueAt(linhaS,0).toString());
            Clientes c = this.daocliente.findClientes(codigo);
            tfCodigo.setText(String.valueOf(c.getCodigo()));
            tfNome.setText(c.getNome());
        }
    }
        
    private void limparCampos()
    {
        tfCodigo.setText("");
        tfNome.setText("");
    }
    
    private void atualizaTabela()
    {
        List<Clientes> lista = this.daocliente.findClientesEntities();
        DefaultTableModel tabela = (DefaultTableModel) tableClientes.getModel();
        tabela.setRowCount(0);
        
        for (Clientes c : lista)
        {
            Object[] obj = new Object[] {c.getCodigo(),c.getNome()};
            tabela.addRow(obj);
        }
        
    }

