    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int codigo = Integer.parseInt(tfCodigo.getText());
        Clientes c = this.daocliente.findClientes(codigo);
        c.setNome(tfNome.getText());
        
        try {
            this.daocliente.edit(c);
            JOptionPane.showMessageDialog(this, "Cliente atualizado!");
            limparCampos();
            atualizaTabela();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar!");
        }
        
    }//GEN-LAST:event_btEditarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int codigo = Integer.parseInt(tfCodigo.getText());
        
        
        try {
            this.daocliente.destroy(codigo);
            JOptionPane.showMessageDialog(this, "Cliente excluído!");
            limparCampos();
            atualizaTabela();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir!");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

